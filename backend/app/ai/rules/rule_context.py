"""从旧测试用例生成器迁移来的规则上下文。

这些内容用于动态注入 Prompt，避免把所有测试方法论硬编码在单个 Prompt 中。
后续可以替换为数据库规则库，例如 `nb_test_test_rules`。
"""


TEST_DESIGN_RULES = """
## 测试设计方法
1. EP 等价类划分：适用于输入范围、格式、枚举、必填、类型限制。
   - 有效等价类可合并覆盖；无效等价类应单独覆盖。
2. BVA 边界值分析：适用于数值、长度、数量、时间、金额边界。
   - 覆盖边界值、边界内、边界外。
3. ST 场景法：适用于业务流程、多步骤操作、状态流转、页面跳转。
   - 至少覆盖基本流，必要时补充备选流和异常流。
4. EG 错误推测：适用于高风险、历史缺陷多、输入异常、并发、弱网、安全场景。
   - 输入框补充特殊字符、SQL 注入、XSS、超长、空值。
   - 提交按钮补充重复提交、快速双击、弱网、防重。
"""


PRIORITY_RULES = """
## 优先级与回归分类
- P0：核心基本流、核心有效等价类，对应 SMOKE。
- P1：备选流、边界值、主要无效等价类，对应 CORE。
- P2：异常流、常见错误推测，对应 FULL。
- P3：边缘错误推测、低频兼容场景，对应 FULL。
- 涉及金额、安全、权限、高频功能、外部依赖时，优先级提升一级。
- 低频、仅体验影响、有降级方案时，可降低一级。
"""


TRACEABILITY_RULES = """
## 需求追溯与覆盖
- 每条用例必须填写 source_requirement_ids，且必须来自输入需求 req_key。
- 每条需求至少关联 1 条用例。
- 复杂或高风险需求建议覆盖深度 >= 5 条/需求。
- 禁止生成没有需求来源的孤儿用例。
- 生成结果必须支持需求 -> 用例、用例 -> 需求的双向追溯。
"""


PARSING_RULES = """
## 需求解析触发规则
- 用户故事：识别“作为...我想要...以便于...”。
- 验收条件：识别“验收条件”“AC”“必须”“应该”“需要”。
- 业务规则：识别“规则”“限制”“约束”“如果...则...”“当...时...”。
- 异常场景：识别“异常”“错误”“失败”“超时”“取消”“中断”。
- 边界：识别“最多”“至少”“范围”“长度”“数量”“N分钟内”。
- 歧义：识别“合理”“适当”“足够”“多个”“正常显示”“正确处理”等模糊词。
"""


BUSINESS_RULES = """
## 通用业务测试规则
- 列表：空列表、大数据量、分页、筛选、搜索、排序、批量操作、导出一致性、网络异常。
- 输入框：必填、长度边界、特殊字符、SQL 注入、XSS、emoji、前后空格、纯空格、格式校验。
- 下拉选择：默认值、选项展示、搜索无结果、单选/多选、必选校验、回显。
- 按钮：禁用态、加载态、防重复点击、弱网防重。
- 提交/保存：校验拦截、提交成功、正常网络防重、弱网防重。
- 取消：二次确认、确认取消后不保存数据。
- 文件/图片上传：支持格式、不支持格式、大小限制、数量限制、成功预览、上传失败、删除、必传。
- 详情/展示：每个字段展示正确，枚举/状态展示正确，条件字段显示/隐藏正确。
"""


DEDUP_RULES = """
## 去重规则
- EP 无效类与 EG 错误推测语义相同，应合并保留更具体的用例。
- BVA 离点与 EP 无效类边界语义相同，应合并并保留具体边界值。
- ST 异常流与 EG 错误推测语义相同，应保留完整流程。
- 同一验证目标不得因为“仅数据状态不同”重复生成。
- 合并时保留全部 design_methods，例如 ["EP", "EG"]。
- 不跨不同 source_requirement_ids 合并。
"""


def get_case_generation_rules() -> str:
    return "\n\n".join(
        [
            TEST_DESIGN_RULES.strip(),
            PRIORITY_RULES.strip(),
            TRACEABILITY_RULES.strip(),
            PARSING_RULES.strip(),
            BUSINESS_RULES.strip(),
            DEDUP_RULES.strip(),
        ]
    )
